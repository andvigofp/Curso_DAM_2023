package Ejercicios1.Ejercicio10;

public class NumeroUno_CienFor {
    public static void NumeroFor() {
        /**
         * Escribir los números del 1 al 100 utilizando ciclo for
         */
        //Variable tipo entero, iniciamos en 0
        int numero1=0;
        //Utilizamos el bucle for para que recorra los números entre 1 y 100
        //Inicio
        for (numero1=1; numero1<=100; numero1++ ) {
            //Imprime por pantlla los números del 1 y 100
            System.out.println("Los números son: " + numero1);
        }
        //Fin
    }
}
